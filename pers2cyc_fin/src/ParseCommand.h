bool ParseCommand(int argc, char** argv, std::string &point_file, int &numbars, bool &verbose, int &index, int &gap){
	try
	{
		/* Define the program options description
		*/
		namespace po = boost::program_options;
		po::options_description desc("loopGeom Usage");
		desc.add_options()
			(",h", "Help information;")
			(",l", "License information;")
			(",n", po::value<int>(&numbars)->default_value(1), "Number of 2-cycles to output")			
            (",d", po::value<int>(&index)->default_value(0), "Which bar from largest")
            (",g", po::value<int>(&gap)->default_value(0), "Gap")
			(",i", po::value<std::string>(&point_file)->default_value(""), "The file contains points in persues stlye. See Gudhi documentation")
            (",v", po::value<bool>(&verbose)->default_value(false), "Display simplex values and program calculation");
			//(",s", po::value<std::string>(&output_persistence_file_name)->default_value("pers"), "The file name for the output 				persistence barcodes of input simplicial maps (default value: \"pers\")");
		// Parser map
		po::variables_map vm;
		try
		{
			po::store(po::parse_command_line(argc, argv, desc), vm);

			//
			if (vm.count("-h"))
			{
				std::cout << desc << std::endl;
				exit(EXIT_SUCCESS);
			}
			//
			if (vm.count("-l"))
			{
				std::cout << strLicense << std::endl;
				exit(EXIT_SUCCESS);
			}
			//
			po::notify(vm);
		}
		catch (boost::program_options::required_option& p)
		{
			std::cerr << "ERROR: " << p.what() << std::endl;
			return false;
		}
	}
	catch (std::exception& i)
	{
		std::cerr << "Unhandled Exception reached the top of main: "
			<< i.what() << ", application will now exit" << std::endl;
		return false;

	}
	return true;
}	//end ParseCommand
